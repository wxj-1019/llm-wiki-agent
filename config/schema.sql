-- ═══════════════════════════════════════════════════════════════════════════
-- 1. wiki_pages — core content table with full-text search
-- ═══════════════════════════════════════════════════════════════════════════
CREATE TABLE wiki_pages (
    id              SERIAL PRIMARY KEY,
    path            TEXT UNIQUE NOT NULL,              -- 'wiki/sources/my-page.md'
    title           TEXT NOT NULL,
    page_type       TEXT NOT NULL DEFAULT 'source',    -- source/entity/concept/synthesis
    tags            TEXT[] DEFAULT '{}',
    body            TEXT NOT NULL,
    body_tsv        TSVECTOR,                          -- auto-generated by trigger
    quality_score   REAL DEFAULT 0,
    source_url      TEXT,
    source_type     TEXT,                               -- web/rss/arxiv/github
    extractor       TEXT,
    created_at      TIMESTAMPTZ DEFAULT NOW(),
    updated_at      TIMESTAMPTZ DEFAULT NOW(),

    CONSTRAINT valid_page_type CHECK (page_type IN ('source','entity','concept','synthesis'))
);

-- FTS index: try zhparser first, fallback to simple with bigram application-level tokenization
DO $$
BEGIN
    IF EXISTS (SELECT 1 FROM pg_extension WHERE extname = 'zhparser') THEN
        CREATE TEXT SEARCH CONFIGURATION zh_cfg (PARSER = zhparser);
        ALTER TEXT SEARCH CONFIGURATION zh_cfg
            ADD MAPPING FOR n,v,a,i,e,l,j WITH simple;
        EXECUTE 'CREATE INDEX idx_wiki_fts ON wiki_pages USING GIN (body_tsv)';
    ELSE
        -- Fallback: use 'simple' config, application handles CJK bigram tokenization
        EXECUTE 'CREATE INDEX idx_wiki_fts ON wiki_pages USING GIN (body_tsv)';
    END IF;
END $$;

CREATE INDEX idx_wiki_type_time ON wiki_pages (page_type, updated_at DESC);
CREATE INDEX idx_wiki_source_url ON wiki_pages (source_url) WHERE source_url IS NOT NULL;
CREATE INDEX idx_wiki_tags ON wiki_pages USING GIN (tags);


-- ═══════════════════════════════════════════════════════════════════════════
-- 2. wiki_embeddings — vector embeddings (pgvector)
-- ═══════════════════════════════════════════════════════════════════════════
CREATE TABLE wiki_embeddings (
    id              SERIAL PRIMARY KEY,
    page_path       TEXT UNIQUE NOT NULL REFERENCES wiki_pages(path) ON DELETE CASCADE,
    embedding       HALFVEC(768),          -- nomic-embed-text = 768 dims, halfvec saves 50% space
    model           TEXT NOT NULL DEFAULT 'nomic-embed-text',
    updated_at      TIMESTAMPTZ DEFAULT NOW()
);

-- HNSW index: faster queries than IVFFlat, suitable for <100k vectors
CREATE INDEX idx_wiki_embedding ON wiki_embeddings
    USING hnsw (embedding halfvec_cosine_ops)
    WITH (m = 16, ef_construction = 200);


