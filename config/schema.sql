-- Auto-update body_tsv on INSERT/UPDATE
CREATE OR REPLACE FUNCTION wiki_tsv_trigger() RETURNS trigger AS $$
DECLARE
    cfg_name TEXT;
BEGIN
    -- Allow callers to supply their own body_tsv (e.g. CJK bigram fallback)
    IF NEW.body_tsv IS NOT NULL THEN
        RETURN NEW;
    END IF;

    -- Check if zhparser config exists
    IF EXISTS (SELECT 1 FROM pg_ts_config WHERE cfgname = 'zh_cfg') THEN
        cfg_name := 'zh_cfg';
    ELSE
        cfg_name := 'simple';
    END IF;
    NEW.body_tsv := to_tsvector(cfg_name, coalesce(NEW.title, '') || ' ' || coalesce(NEW.body, ''));
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS wiki_tsv_update ON wiki_pages;
CREATE TRIGGER wiki_tsv_update
    BEFORE INSERT OR UPDATE ON wiki_pages
    FOR EACH ROW EXECUTE FUNCTION wiki_tsv_trigger();

-- Auto-update updated_at
CREATE OR REPLACE FUNCTION update_timestamp() RETURNS trigger AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS wiki_pages_updated ON wiki_pages;
CREATE TRIGGER wiki_pages_updated
    BEFORE UPDATE ON wiki_pages
    FOR EACH ROW EXECUTE FUNCTION update_timestamp();

DROP TRIGGER IF EXISTS wiki_embeddings_updated ON wiki_embeddings;
CREATE TRIGGER wiki_embeddings_updated
    BEFORE UPDATE ON wiki_embeddings
    FOR EACH ROW EXECUTE FUNCTION update_timestamp();


-- ═══════════════════════════════════════════════════════════════════════════
-- Hybrid search function
-- ═══════════════════════════════════════════════════════════════════════════

CREATE OR REPLACE FUNCTION hybrid_search(
    query_text      TEXT,
    query_embedding HALFVEC,
    result_limit    INT DEFAULT 20,
    fts_weight      REAL DEFAULT 0.6,
    vec_weight      REAL DEFAULT 0.4
) RETURNS TABLE (
    path            TEXT,
    title           TEXT,
    page_type       TEXT,
    excerpt         TEXT,
    hybrid_score    REAL,
    updated_at      TIMESTAMPTZ
) AS $$
DECLARE
    ts_config TEXT;
BEGIN
    IF EXISTS (SELECT 1 FROM pg_ts_config WHERE cfgname = 'zh_cfg') THEN
        ts_config := 'zh_cfg';
    ELSE
        ts_config := 'simple';
    END IF;

    RETURN QUERY
    WITH fts_results AS (
        SELECT
            wp.path,
            wp.title,
            wp.page_type,
            ts_rank(wp.body_tsv, websearch_to_tsquery(ts_config, query_text)) AS fts_rank,
            LEFT(wp.body, 300) AS body_excerpt,
            wp.updated_at
        FROM wiki_pages wp
        WHERE wp.body_tsv @@ websearch_to_tsquery(ts_config, query_text)
        LIMIT result_limit * 3
    ),
    vec_results AS (
        SELECT
            we.page_path,
            1 - (we.embedding <=> query_embedding) AS vec_score
        FROM wiki_embeddings we
        WHERE we.embedding IS NOT NULL
        ORDER BY we.embedding <=> query_embedding
        LIMIT result_limit * 3
    )
    SELECT
        COALESCE(f.path, v.page_path) AS path,
        COALESCE(f.title, '') AS title,
        COALESCE(f.page_type, 'unknown') AS page_type,
        COALESCE(f.body_excerpt, '') AS excerpt,
        (fts_weight * COALESCE(f.fts_rank, 0) + vec_weight * COALESCE(v.vec_score, 0)) AS hybrid_score,
        f.updated_at
    FROM fts_results f
    FULL OUTER JOIN vec_results v ON f.path = v.page_path
    ORDER BY hybrid_score DESC
    LIMIT result_limit;
END;
$$ LANGUAGE plpgsql STABLE;


